
/**
* op_add - Sum two integer
* @a: The first integer
* @b: The second integer
*
* Return: The sum of the integers
*/

int op_add(int a, int b)
{
return (a + b);
}

/**
* op_sub - Subtract two integer
* @a: The first integer
* @b: The second integer
*
* Return: The different of the integers
*/

int op_sub(int a, int b)
{
return (a - b);
}

/**
* op_div - Divides two integer
* @a: The first integer
* @b: The second integer
*
* Return: The division of the integers
*/

int op_div(int a, int b)
{
return (a / b);
}

/**
* op_mod - Subtract two integer
* @a: The first integer
* @b: The second integer
*
* Return: The modulus of the two integer
*/

int op_mod(int a, int b)
{
return (a % b);
}

/**
* op_mul - Subtract two integer
* @a: The first integer
* @b: The second integer
*
* Return: The result of multiplying the two integer
*/

int op_mul(int a, int b)
{
return (a * b);
}
